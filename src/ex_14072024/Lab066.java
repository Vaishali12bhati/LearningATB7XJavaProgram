public class Lab066 {
    public static void main(String[] args) {
        //incerement(+++ ,decrement (--)
        //pre increment  post increment
        // pre decrement post decrement


        //pre increment
        //value is increased first and then store in the result
        int a = 45;
        int c = 20;
        int b = ++a;
        int d  = a;
        System.out.println(a);//pre
        System.out.println(b); //pre
        System.out.println(++c); //pre
        System.out.println(d++);//post


    }
}
