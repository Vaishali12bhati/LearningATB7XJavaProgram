package July.ex_13072024;
//escape examples
public class Lab042 {
    public static void main(String[] args) {
        char a = '\t'; //tabspace add a tab
        char b = '\b'; //backspace deleter previous one character
        char c = '\n'; //newline gives a new line
        char d= '\r';  //carriage return //delete the previous word
        char e =  '\f'; //formfeed
        char f=  '\'';  //single quote character
        char g= '\"'; //double quotes character
        char h =  '\\'; //backslash character //add a slash
        System.out.println("A RESULT"+""+"pramod"+ a+"dutta"); //add a tab
        System.out.println("B RESULT"+""+"vaishali"+b+"bhati"); //onecharacter deleted
        System.out.println("C RESULT"+""+"vaishali"+c+"bhati"); //give a new line
        System.out.println("D RESULT"+""+"vaishali"+d+"bhati"); // delete a word
        System.out.println("E RESULT"+""+"vaishali"+e+"bhati"); // formfeed icon added
        System.out.println("F RESULT"+""+"vaishali"+f+"bhati"); //single quotes added
        System.out.println("G RESULT"+""+"vaishali"+g+"bhati");// double quotes added
        System.out.println("H RESULT"+""+"vaishali"+h+"bhati"); // add a slash
    }
}
